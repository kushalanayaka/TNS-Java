package Practice;

public class ConditionalStatements {
	
	public static void main(String args[]) {
		
		for(int i = 5; i < 10; i++) {
			
			if( i == 5)
				break;
		    
			System.out.println(i);
			
		}
		
		
		for(int i = 2 ; i <= 2 ; i++) {
			
			for(int j = 1; j <= 10 ; j++) {
				
				System.out.println( i + " X " + j + "= " + i * j);
				
			}
		}
		
	}

}




